package com.shop.vo.car;

import com.shop.vo.BaseVO;
import lombok.Getter;
import lombok.ToString;

import java.math.BigDecimal;

@ToString
@Getter
public class ShopCarVO extends BaseVO {


    private Long id;

    private Long userId;

    /**
     * 商品名称
     */
    private String proName;

    /**
     * 商品id
     */
    private Integer proId;

    /**
     * 商品logo
     */
    private String proLogo;

    /**
     * 价格（分）
     */
    private BigDecimal proPrice;

    /**
     * 商品数量
     */
    private Integer proNum;

    /**
     * 来源
     */
    private Integer source;

    /**
     * 购物车状态0：未删除，1：已删除
     */
    private Integer carStatus;

    public ShopCarVO setId(Long id) {
        this.id = id;
        return this;
    }

    public ShopCarVO setUserId(Long userId) {
        this.userId = userId;
        return this;
    }

    public ShopCarVO setProName(String proName) {
        this.proName = proName;
        return this;
    }

    public ShopCarVO setProId(Integer proId) {
        this.proId = proId;
        return this;
    }

    public ShopCarVO setProLogo(String proLogo) {
        this.proLogo = proLogo;
        return this;
    }

    public ShopCarVO setProPrice(BigDecimal proPrice) {
        this.proPrice = proPrice;
        return this;
    }

    public ShopCarVO setProNum(Integer proNum) {
        this.proNum = proNum;
        return this;
    }

    public ShopCarVO setSource(Integer source) {
        this.source = source;
        return this;
    }

    public ShopCarVO setCarStatus(Integer carStatus) {
        this.carStatus = carStatus;
        return this;
    }
}